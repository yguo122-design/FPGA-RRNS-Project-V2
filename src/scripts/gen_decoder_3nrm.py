"""
gen_decoder_3nrm.py
Generates decoder_3nrm.v - 3NRM-RRNS decoder with 84 parallel MRC channels.

Moduli: m0=64, m1=63, m2=65, m3=31, m4=29, m5=23, m6=19, m7=17, m8=11
Codeword bit layout (right-aligned in 64-bit bus):
  [47:42] r0 = x%64  (6b)
  [41:36] r1 = x%63  (6b)
  [35:29] r2 = x%65  (7b)
  [28:24] r3 = x%31  (5b)
  [23:19] r4 = x%29  (5b)
  [18:14] r5 = x%23  (5b)
  [13:9]  r6 = x%19  (5b)
  [8:4]   r7 = x%17  (5b)
  [3:0]   r8 = x%11  (4b)

Pipeline stages:
  Stage 0 (input reg):  latch residues_in, start
  Stage 1 (MRC step1):  compute a1=ri, diff2=(rj-ri), diff3=(rk-ri)  [per channel]
  Stage 2 (MRC step2):  compute a2 = (diff2 * INV_IJ) % mj           [per channel]
  Stage 3 (MRC step3):  compute a3_raw = diff3 - a2*mi               [per channel]
  Stage 4 (MRC step4):  compute a3 = (a3_raw * INV_IJMK) % mk        [per channel]
  Stage 5 (MRC step5):  compute X = a1 + a2*mi + a3*mi*mj            [per channel]
  Stage 6 (dist calc):  compute Hamming distance for each channel X   [per channel]
  Stage 7 (MLD-A):      find partial min over ch0..41 and ch42..83
  Stage 8 (MLD-B):      final min, output data_out/valid/uncorrectable

Total latency: 9 clock cycles (start -> valid)
"""

from math import gcd

# ============================================================
# Math helpers
# ============================================================
def extended_gcd(a, b):
    if a == 0:
        return b, 0, 1
    g, x, y = extended_gcd(b % a, a)
    return g, y - (b // a) * x, x

def mod_inv(a, m):
    a = a % m
    if gcd(a, m) != 1:
        return None
    _, x, _ = extended_gcd(a, m)
    return x % m

