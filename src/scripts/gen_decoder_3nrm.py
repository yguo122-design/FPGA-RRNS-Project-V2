L("    // Use direct % operator (synthesizable, ~8 LUT levels for 16-bit input)")
L("    wire [6:0] cand_r [0:83][0:8];")
L()

for idx in range(84):
    for m_idx, m in enumerate(MODS):
        L(f"    assign cand_r[{idx}][{m_idx}] = ch_x[{idx}] % 7'd{m};")

L()
L("    // Stage 6a pipeline registers: register candidate residues + received residues")
L("    reg [6:0] cand_r_s6a [0:83][0:8];")
L("    reg [6:0] recv_r_s6a [0:8];")
L("    reg [15:0] ch_x_s6a  [0:83];")
L("    reg        ch_valid_s6a;")
L()
L("    integer ci6a, mi6a;")
L("    always @(posedge clk or negedge rst_n) begin")
L("        if (!rst_n) begin")
L("            for (ci6a=0; ci6a<84; ci6a=ci6a+1)")
L("                for (mi6a=0; mi6a<9; mi6a=mi6a+1)")
L("                    cand_r_s6a[ci6a][mi6a] <= 7'd0;")
L("            for (mi6a=0; mi6a<9; mi6a=mi6a+1)")
L("                recv_r_s6a[mi6a] <= 7'd0;")
L("            for (ci6a=0; ci6a<84; ci6a=ci6a+1)")
L("                ch_x_s6a[ci6a] <= 16'd0;")
L("            ch_valid_s6a <= 1'b0;")
L("        end else begin")
L("            for (ci6a=0; ci6a<84; ci6a=ci6a+1)")
