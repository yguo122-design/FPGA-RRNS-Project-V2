L("        end")
L("    end")
L()
L("    // =========================================================================")
L("    // 5. MLD Stage B + Output Register (Stage 8)")
L("    // Final comparison: mid_a vs mid_b -> select global minimum.")
L("    // Group A wins ties (lower channel index priority).")
L("    // NRM_MAX_ERRORS = 3 (can correct up to 3 erroneous residues out of 9)")
L("    // =========================================================================")
L("    localparam NRM_MAX_ERRORS = 3;")
L()
L("    always @(posedge clk or negedge rst_n) begin")
L("        if (!rst_n) begin")
L("            data_out      <= 16'd0;")
L("            valid         <= 1'b0;")
L("            uncorrectable <= 1'b0;")
L("        end else begin")
L("            valid <= mid_valid_reg;")
L("            if (mid_valid_reg) begin")
L("                // Group A wins on tie")
L("                if (mid_dist_a_reg <= mid_dist_b_reg) begin")
L("                    data_out      <= mid_x_a_reg;")
L("                    uncorrectable <= (mid_dist_a_reg > NRM_MAX_ERRORS);")
L("                end else begin")
L("                    data_out      <= mid_x_b_reg;")
L("                    uncorrectable <= (mid_dist_b_reg > NRM_MAX_ERRORS);")
L("                end")
L("            end else begin")
L("                uncorrectable <= 1'b0;")
L("            end")
L("        end")
L("    end")
L()
L("endmodule")

