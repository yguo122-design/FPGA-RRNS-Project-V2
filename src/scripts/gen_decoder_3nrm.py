L("        end")
L("    end")
L()
L("    // Stage 6b: Compute Hamming distance for each channel")
L("    wire [3:0] dist [0:83];")
L()
for idx in range(84):
    terms = " + ".join([f"(cand_r_s6a[{idx}][{m}] != recv_r_s6a[{m}] ? 4'd1 : 4'd0)" for m in range(9)])
    L(f"    assign dist[{idx}] = {terms};")

L()
L("    // Stage 6b pipeline registers")
L("    reg [3:0]  dist_s6b  [0:83];")
L("    reg [15:0] ch_x_s6b  [0:83];")
L("    reg        ch_valid_s6b;")
L()
L("    integer ci6b;")
L("    always @(posedge clk or negedge rst_n) begin")
L("        if (!rst_n) begin")
L("            for (ci6b=0; ci6b<84; ci6b=ci6b+1) begin")
L("                dist_s6b[ci6b] <= 4'd9;")
L("                ch_x_s6b[ci6b] <= 16'd0;")
L("            end")
L("            ch_valid_s6b <= 1'b0;")
L("        end else begin")
L("            for (ci6b=0; ci6b<84; ci6b=ci6b+1) begin")
L("                dist_s6b[ci6b] <= dist[ci6b];")
L("                ch_x_s6b[ci6b] <= ch_x_s6a[ci6b];")
L("            end")
L("            ch_valid_s6b <= ch_valid_s6a;")
L("        end")
L("    end")
L()
L("    // =========================================================================")
L("    // 4. MLD Stage A: Two Parallel Partial Minimum Finders (Stage 7)")
L("    // Group A: ch0..41  (42 channels)")
L("    // Group B: ch42..83 (42 channels)")
L("    // =========================================================================")
L("    reg [3:0]  mid_dist_a_comb, mid_dist_b_comb;")
L("    reg [15:0] mid_x_a_comb,    mid_x_b_comb;")
L("    integer    jj;")
L()
L("    always @(*) begin")
L("        // Group A: ch0..41")
L("        mid_dist_a_comb = 4'd9;")
L("        mid_x_a_comb    = 16'd0;")
L("        for (jj = 0; jj <= 41; jj = jj + 1) begin")
L("            if (dist_s6b[jj] < mid_dist_a_comb) begin")
L("                mid_dist_a_comb = dist_s6b[jj];")
L("                mid_x_a_comb    = ch_x_s6b[jj];")
L("            end")
L("        end")
L("        // Group B: ch42..83")
L("        mid_dist_b_comb = 4'd9;")
L("        mid_x_b_comb    = 16'd0;")
L("        for (jj = 42; jj <= 83; jj = jj + 1) begin")
L("            if (dist_s6b[jj] < mid_dist_b_comb) begin")
L("                mid_dist_b_comb = dist_s6b[jj];")
L("                mid_x_b_comb    = ch_x_s6b[jj];")
L("            end")
L("        end")
L("    end")
L()
L("    (* dont_touch = \"true\" *) reg [3:0]  mid_dist_a_reg, mid_dist_b_reg;")
L("    (* dont_touch = \"true\" *) reg [15:0] mid_x_a_reg,    mid_x_b_reg;")
L("    (* dont_touch = \"true\" *) reg        mid_valid_reg;")
L()
L("    always @(posedge clk or negedge rst_n) begin")
L("        if (!rst_n) begin")
L("            mid_dist_a_reg <= 4'd9;")
L("            mid_dist_b_reg <= 4'd9;")
L("            mid_x_a_reg    <= 16'd0;")
L("            mid_x_b_reg    <= 16'd0;")
L("            mid_valid_reg  <= 1'b0;")
L("        end else begin")
L("            mid_dist_a_reg <= mid_dist_a_comb;")
L("            mid_dist_b_reg <= mid_dist_b_comb;")
L("            mid_x_a_reg    <= mid_x_a_comb;")
L("            mid_x_b_reg    <= mid_x_b_comb;")
