# Algorithm parameters
# ============================================================
MODS = [64, 63, 65, 31, 29, 23, 19, 17, 11]
MOD_BITS = [6, 6, 7, 5, 5, 5, 5, 5, 4]   # ceil(log2(mod))
# Bit positions in the 48-bit codeword (MSB first, right-aligned in 64-bit)
# [47:42]=r0, [41:36]=r1, [35:29]=r2, [28:24]=r3, [23:19]=r4,
# [18:14]=r5, [13:9]=r6, [8:4]=r7, [3:0]=r8
BIT_HI = [47, 41, 35, 28, 23, 18, 13, 8, 3]
BIT_LO = [42, 36, 29, 24, 19, 14,  9, 4, 0]

# Generate all 84 triplets
triplets = []
for i in range(9):
    for j in range(i+1, 9):
        for k in range(j+1, 9):
            triplets.append((i, j, k))

assert len(triplets) == 84

# Precompute MRC constants for each triplet
def get_mrc_constants(i, j, k):
    mi, mj, mk = MODS[i], MODS[j], MODS[k]
    inv_ij  = mod_inv(mi, mj)          # Inv(mi mod mj, mj)
    inv_ijk = mod_inv((mi * mj) % mk, mk)  # Inv(mi*mj mod mk, mk)
    period  = mi * mj * mk             # Not needed in hardware but useful for range
    return inv_ij, inv_ijk, mi, mj, mk

# Verify MRC for all triplets
def verify_mrc(test_vals=[0, 1, 100, 12345, 65535]):
    for tv in test_vals:
        rs = [tv % m for m in MODS]
        for idx, (i, j, k) in enumerate(triplets):
            mi, mj, mk = MODS[i], MODS[j], MODS[k]
            inv_ij, inv_ijk, _, _, _ = get_mrc_constants(i, j, k)
            a1 = rs[i]
            a2 = ((rs[j] - a1) * inv_ij) % mj
            a3_raw = rs[k] - a1 - a2 * mi
            a3 = (a3_raw * inv_ijk) % mk
            X = a1 + a2 * mi + a3 * mi * mj
            # X should equal tv (mod mi*mj*mk), and since tv < 65536 < mi*mj*mk for most triplets
            # we check X == tv directly
            if X != tv:
                # X might be tv + n*period for some n, check modular equivalence
                period = mi * mj * mk
                if X % period != tv % period:
                    print(f"ERROR: triplet {idx} ({mi},{mj},{mk}), tv={tv}, X={X}")
                    return False
    return True

ok = verify_mrc()
print(f"MRC verification: {'PASS' if ok else 'FAIL'}")

